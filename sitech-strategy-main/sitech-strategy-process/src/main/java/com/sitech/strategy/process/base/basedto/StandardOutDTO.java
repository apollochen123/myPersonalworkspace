package com.sitech.strategy.process.base.basedto;

import com.sitech.jcfx.dt.MBean;
import com.sitech.jcfx.dt.out.OutDTO;

/**
 * 标准输出DTO
* @date: 2016-9-5 
* @author: sunliang 
* @title: StandardOutDTO 
* @version: 1.0 
* @description： 
* update_version: update_date: update_author: update_note:
 */
public class StandardOutDTO extends OutDTO {
	
	private static final long serialVersionUID = -8512527356609332824L;
	/**
	 * MBean存储
	 */
	private MBean bean;
	
	/**
	 * 
	* @date: 2016-9-5 
	* @author: sunliang 
	* @title: encode 
	* @return 
	* @exception: 
	* @version: 1.0 
	* @description: 
	* update_version: update_date: update_author: update_note:
	 */
	@Override
	public MBean encode() {
		return getBean();
	}

	/**
	 * 
	* @date: 2016-9-5 
	* @author: sunliang 
	* @title: getBean 
	* @return 
	* @exception: 
	* @version: 1.0 
	* @description: 
	* update_version: update_date: update_author: update_note:
	 */
	public MBean getBean() {
        return bean;
    }

	/**
	 * 
	* @date: 2016-9-5 
	* @author: sunliang 
	* @title: setBean 
	* @param bean 
	* @exception: 
	* @version: 1.0 
	* @description: 
	* update_version: update_date: update_author: update_note:
	 */
    public void setBean(MBean bean) {
        this.bean = bean;
    }

}
